    private void CargarComboCategorias(){
    DefaultComboBoxModel model = new DefaultComboBoxModel ();
    
    Connection _conexion = null;
    try {
        String conexionString ="jdbc:mysql://localhost/crm?characterEncoding=latin1";
        String driverName ="com.mysql.cj.jdbc.Driver";  //com.mysql.jdbc.Driver;
        Class.forName(driverName).newInstance();
       _conexion = DriverManager.getConnection(conexionString, "root","012003"); 
       _conexion.setTransactionIsolation(Connection.TRANSACTION_READ_COMMITTED);
      
       Statement st = _conexion.createStatement();
       ResultSet rs= st.executeQuery ("select Idcategoria, Nombre from categoriaproductos\n" +
                   "order by Nombre asc");
       while (rs.next()){
           
           ComboBox C =new ComboBox(rs.getInt("Idcategoria"),rs.getString("Nombre"));
           model.addElement(C);
           
           
           //System.out.println(rs.getInt("Idcategoria"));
           //System.out.println(rs.getString("Nombre"));
           //System.out.println("");
       }
      // System.out.println("Conexion exitosa!!!");
    rs.close();
    
    jComboBoxCategoria.setModel(model);
            
    }catch (Exception ex){ 
        System.out.println("Error" + ex.getMessage());
    
    }finally {
        try {
        _conexion.close();
   }catch (Exception ex2){   
   }        
} 
    
}
